                <h3 class="pt-20 pb-20">
                4.2 Remote or isolated work
                </h3>

                <p class="pb-10">As a person conducting a business or undertaking (PCBU) you must manage the risks associated with remote or isolated work, including ensuring effective communication with the worker carrying out remote or isolated work.</p>

                <p class="pb-10">Remote or isolated work is work that is isolated from the assistance of other people because of the location, time or nature of the work being done. Assistance from other people includes rescue, medical assistance and emergency services.</p>
                
                <p class="pb-10">A worker may be isolated even if other people may be close by, for example a cleaner working by themselves at night in a city office building. In other cases, a worker may be far away from populated areas, for example on a farm.</p>
                
                <p class="pb-10">
                    Remote and isolated work includes:
                    <ul>
                        <li>all-night convenience store and service station attendants</li>
                        <li>sales representatives, including real estate agents</li>
                        <li>long-distance freight transport drivers</li>
                        <li>scientists, park rangers and others carrying out fieldwork alone or in remote locations</li>
                        <li>health and community workers working in isolation with members of the public</li>
                    </ul>
                </p>

                <p>In some situations, a worker may be alone for a short time. In other situations, the worker may be on their own for days or weeks in remote locations, for example on sheep and cattle stations.</p>
                
                <h3 class="pt-20 pb-20">Assessing the risks</h3>
                <p>Working alone or remotely increases the risk of any job. Exposure to violence and poor access to emergency assistance are the main hazards that increase the risk of remote or isolated work. Factors that should be considered when assessing the risks include the following.</p>


                <p>The length of time the person may be working alone:
                    <ul>
                        <li>How long will the person need to be alone to finish the job?</li>
                    </ul>
                </p>

                <p>The time of day when a person may be working alone:
                    <ul>
                        <li>Is there increased risk at certain times of day? For example, a service station attendant working alone late at night may be at greater risk of exposure to violence.</li>
                    </ul>
                </p>

                <h3 class="pt-20 pb-20">Communication</h3>
                <p>
                    <ul>
                        <li>What forms of communication does the worker have access to?</li>
                        <li>Are there procedures for regular contact with the worker?</li>
                        <li>Will the emergency communication system work properly in all situations?</li>
                        <li>If communication systems are vehicle-based, what arrangements are there to cover the worker when he or she is away from the vehicle?</li>
                    </ul>
                </p>

                <h3 class="pt-20 pb-20">The location of the work</h3>

                <p>
                    <ul>
                        <li>Is the work in a remote location that makes immediate rescue or attendance of emergency services difficult?</li>
                        <li>What is likely to happen if there is a vehicle breakdown?</li>
                    </ul>
                </p>

                <h3 class="pt-20 pb-20">The nature of the work</h3>

                <p>
                    <ul>
                        <li>What machinery, tools and equipment may be used?</li>
                        <li>Are high risk activities involved? For example work at heights, work with electricity, hazardous substances or hazardous plant.</li>
                        <li>Is fatigue likely to increase risk (for example, with long hours driving a vehicle or operating machinery)?</li>
                        <li>Is there an increased risk of violence or aggression when workers have to deal with clients or customers by themselves?</li>
                        <li>Can environmental factors affect the safety of the worker? For example, exposure to extreme hot or cold environment?</li>
                        <li>Is there risk of attack by an animal, including reptiles, insects and sea creatures?</li>
                    </ul>
                </p>

                <h3 class="pt-20 pb-20">The skills and capabilities of the worker</h3>
                <p>
                    <ul>
                        <li>What is the worker’s level of work experience and training? Is the worker able to make sound judgements about his or her own safety?</li>
                        <li>Are you aware of a pre-existing medical condition that may increase risk?</li>
                    </ul>
                </p>

                <h3 class="pt-20 pb-20">Controlling the risks</h3>

                <p class="pb-10">
                    <strong>Buddy system</strong> — some jobs present such a high level of risk that workers should not work alone, for example jobs where there is a risk of violence or where high-powered tools or equipment may be used.
                </p>

                <p class="pb-10">
                    <strong>Workplace layout and design</strong> — workplaces and their surrounds can be designed to reduce the likelihood of violence, for example by installing physical barriers, monitored CCTV and enhancing visibility.
                </p>
                
                <p class="pb-10">
                    <strong>Movement records</strong> – knowing where workers are expected to be can assist in controlling the risks, for example call-in systems with supervisors or colleagues. Satellite tracking systems or devices may also have the capability of sending messages as part of a scheduled call-in system, and have distress or alert functions.
                </p>

                <p class="pb-10">
                    <strong>Training, information and instruction</strong> – as a PCBU you have a duty to provide information, training and instruction suitable for the nature and risks of the work and the controls being put in place to manage the risks. Workers need training to prepare them for working alone and, where relevant, in remote locations. For example training in dealing with potentially aggressive clients, using communications systems, administering first aid, obtaining emergency assistance, driving off-road vehicles or bush survival.
                </p>

                <p class="pb-10">
                    <strong>First aid</strong> – as a PCBU you have specific obligations under the WHS Regulations in relation to first aid requirements in the workplace. Further guidance regarding first aid and supplying first aid kits is located in the Code of Practice: First aid in the workplace.
                </p>

                <p class="pb-10">
                If a worker is working alone in a workplace that has a telephone, communication via the telephone is adequate, provided the worker is able to reach the telephone in an emergency. In situations where a telephone is not available or may not be accessible during an emergency, a method of communication that will allow a worker to call for help in the event of an emergency at any time should be provided, for example:
                </p>

                <p class="pb-10">
                    <ul>
                        <li class="pb-10"><strong>Personal security systems or personal duress systems</strong> – being wireless and portable, these systems are suitable for people who move between different work locations such as health care workers visiting clients or security guards checking otherwise deserted workplaces. Personal security systems need to be able to activate an appropriate safety response. Some personal security systems include a non-movement sensor that will automatically activate an alarm transmission if the transmitter or transceiver has not moved within a certain time.</li>
                        <li class="pb-10"><strong>Radio communication systems</strong> – enable communication between two mobile users in different vehicles or from a mobile vehicle and a fixed station. These systems are dependent upon a number of factors such as frequency, power and distance from or between broadcasters.</li>
                        <li class="pb-10"><strong>Satellite communication systems</strong> – enable communication with workers in geographically remote locations. Satellite phones allow voice transmission during transit, but their operation can be affected by damage to aerials, failure of vehicle power supplies or vehicle damage.</li>
                        <li class="pb-10"><strong>Distress beacons</strong> – can provide pinpoint location and to indicate by activation that an emergency exists. Distress beacons include Emergency Position Indicating Radio Beacons (EPIRB) used in ships and boats, Emergency Locator Transmitters (ELT) used in aircraft and Personal Locator Beacons (PLB) for personal use.</li>
                        <li class="pb-10"><strong>Mobile phones</strong> – cannot be relied upon as an effective means of communication in many locations. Coverage in the area where the worker will work should be confirmed before work starts. Geographical features may impede the use of mobile phones, especially at the edge of the coverage area, and different models have different capabilities in terms of effective range from the base station. Consult the provider if there is doubt about the capability of a particular phone to sustain a signal for the entire period the worker is alone. If gaps in coverage are likely, other methods of communication should be considered. It is important that batteries are kept charged and a spare is available.</li>
                    
                    </ul>
                </p>

                
